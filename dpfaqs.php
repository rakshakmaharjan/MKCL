				<a class="collapsed" data-toggle="collapse" href="#dpfaq1">
					<h5>1. Who is a Depository Participant (DP)? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq1" class="collapse show" data-parent="#accordion">
					<p>A Depository Participant is an agent of CDS and Clearing Limited, who has obtained a registration certificate from SEBON and the membership license from CDSC.<br>
					<strong>Machhapuchchhre Kriti Capital is an authorized Depository Participant (DP) Licensed by SEBON.</strong></p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq2">
					<h5>2. What is a Demat Account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq2" class="collapse" data-parent="#accordion">
					<p>Demat Account is a type of account which electronically holds shares/securities of an individual/institution instead of physical certificate.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq3">
					<h5>3. What are the benefits of opening a demat account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq3" class="collapse" data-parent="#accordion">
					<p style="border-bottom: 0; padding-bottom: 0;">Having a Demat Account helps you avoid several risks and hassles, and hence comes with the following benefits of:</p>
					<ul>
						<li>Eliminating the risk of forgery, theft and the manipulation of share certificates;</li>
						<li>Easy transfer of securities;</li>
						<li>Direct credit of IPO, FPO, right shares, bonus shares to Demat Account.</li>
						<li>Easy pledge for securities in Demat Accounts.</li>
						<li>Easy for the family as well as death name transfers.</li>
					</ul>		
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq4">
					<h5>4. Where can I open Demat Account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq4" class="collapse" data-parent="#accordion">
					<p>To open a new Demat Account, you will have to visit a licensed DP.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq5">
					<h5>5. How can I open Demat Account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq5" class="collapse" data-parent="#accordion">
					<p>It's an easy pie! For a Demat Account, you need to submit a completed Demat Account opening form at DP with relevant documents (copy of your citizenship certificate, photograph, and a copy of your birth certificate copy if you are a minor). You need to make your physical presence for the process though.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq6">
					<h5>6. How many Demat Accounts Can I open? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq6" class="collapse" data-parent="#accordion">
					<p>According to the regulation of SEBON, you as an individual can have a maximum of two Demat Accounts.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq7">
					<h5>7. What is the DP ID, Client ID and BOID? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq7" class="collapse" data-parent="#accordion">
					<p>DP ID is the code number for every licensed DP and Client ID is the auto-created account number from the CDSC System. BOID is the combination of DP ID and Client ID.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq8">
					<h5>8. What is a confirmation letter and an acknowledgement letter? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq8" class="collapse" data-parent="#accordion">
					<p>A confirmation letter and acknowledgment letter mean the same for a Demat Account holder. The letter is an auto-generated copy containing information on Demat Account number and holder’s details when your details are fully entered into the CDSC System.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq9">
					<h5>9. What is Dematerialization (Demat)? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq9" class="collapse" data-parent="#accordion">
					<p>Dematerialization is a process of converting physical securities into electronic form.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq10">
					<h5>10. How can I dematerialize the physical shares? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq10" class="collapse" data-parent="#accordion">
					<p>After opening a Demat Account, you should visit your DP and fill-up a Dematerialization Request Form (DRF) and submit your physical certificates to the DP for further process. Then, your DP creates DRN against the shares & filled DRF. After you are done with your part, you DP takes the process forward. Your DP processes the document to respective RTS for signature verification and completion of the Demat process. If the signatures are verified, the documents (along with share certificates) are accepted by RTS, the share certificates will be deposited in the respective Demat Account within a few days.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq11">
					<h5>11. What is RTS? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq11" class="collapse" data-parent="#accordion">
					<p>RTS or Registrar to Share is a company appointed by the listed company (which has issued shares to the public) for maintenance of their shareholders’ details and facilitation of the shareholders’ transactions.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq12">
					<h5>12. What is DRN? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq12" class="collapse" data-parent="#accordion">
					<p>DRN or Demat Requisition Number is a request-copy for credit and it is issued by DP.</p>
				</div>
						
				<a class="collapsed" data-toggle="collapse" href="#dpfaq13">
					<h5>13. What is done after receiving DRN? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq13" class="collapse" data-parent="#accordion">
					<p>After receiving DRN along with physical certificates, it is handed over to respective RTS.</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq14">
					<h5>14. What if the signature are not verified? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq14" class="collapse" data-parent="#accordion">
					<p>If the signatures fail to get verified, the shares will not be deposited in the Demat Account. In such case, the Demat Account holder should visit the RTS for the required verification process.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq15">
					<h5>15. What should I, as Demat Account Holder, do if the deposited shares are not credited in my Demat Account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq15" class="collapse" data-parent="#accordion">
					<p>You should immediately contact your DP for follow-up and inquire about the delay.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq16">
					<h5>16. What are the probable reasons for rejection of DRF? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq16" class="collapse" data-parent="#accordion">
					<p style="border-bottom: 0; padding-bottom: 0;">The following reasons may cause rejection of DRF:</p>
						<ul>
							<li>If your signature as a Demat Account holder does not match with the record maintained at DP & RTS,</li>
							<li>If you as a shareholder of a company have more than one holder name for the same company, has more than one holder number of the same company.</li>
							<li>If you have filled the form with wrong information of your three consecutive generation lineage (father’s name, grandfather’s name.)</li>
							<li>If the information contained in issued DRN is wrong.</li>
						</ul>	
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq17">
					<h5>17. What is "Mero Share" online application and how to use it? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq17" class="collapse" data-parent="#accordion">
					<p style="border-bottom: 0; padding-bottom: 0;">It is an online service provided by CDS & Clearing limited through which you can have access to your shareholding/transaction details. Following the steps detailed as below, you can use the application:</p>
					<ul>
						<li>Fill up of a request letter for CDS Online Service and submit it to your DP.</li>
						<li>Your DP will then enter your details as provided in the request letter, and you will be provided with your login credentials (Mero Share login as well as a password) through an auto-generated email to your email address.</li>
					</ul>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq18">
					<h5>18. What is the procedure for transfer of holding if the Demat securities are transacted? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq18" class="collapse" data-parent="#accordion">
					<p>If you want to sell your stock, you need to instruct your DP to transfer the securities directly into the Clearing Account of Stock Brokers once the selling information is received from the broker. The same procedure should be completed within T+2 working days. Likewise, if you like to buy stocks, you can give the standing instruction to the DP to credit your account in order to receive the purchased securities. Such instruction can be given at the time of opening Demat Account or after opening the Demat Account. The other way round, you can also instruct your DP for each individual transaction.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq19">
					<h5>19. Is it possible to transfer my stock from one of my DP accounts to another DP account also registered under my name? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq19" class="collapse" data-parent="#accordion">
					<p>Yes, it is possible by transfer shares belonging to the same person from one DP account to another through BO-BO Process. BO stands for Beneficiary Owner. Please consult your DP for further details.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq20">
					<h5>20. How do I buy and trade shares? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq20" class="collapse" data-parent="#accordion">
					<p>You can buy and sell the shares through a stockbroker.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq21">
					<h5>21. What is T+2 Settlement? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq21" class="collapse" data-parent="#accordion">
					<p>T+2 signifies the time period for the settlement of transactions traded in NEPSE. It denotes that settlement should be done within 2 working days of the transaction (buy/sell order execution).
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq22">
					<h5>22. What is Debit Instruction Slip (DIS)? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq22" class="collapse" data-parent="#accordion">
					<p>Debit Instruction Slip (DIS) is an instrument to get shares transferred electronically from one account to another within the same Depository or Inter-Depository.
					</p>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq23">
					<h5>23. What precautions need to be observed by BOs with respect to Debit Instruction Slip (DIS)? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq23" class="collapse" data-parent="#accordion">
					<ul style="border-top: 1px solid #CACFD2">
						<li>Debit Instruction Slips (DIS) are equivalent to the cheque book and must be kept is safe custody.</li>
						<li>If lost or stolen, it should be informed to the DP immediately.</li>
						<li>Unused DIS and Requisition form should be returned to the designated DP while closing Demat Account.</li>
					<ul>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq24">
					<h5>24. What are my duties and responsibilities as a Demat Account holder? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq24" class="collapse" data-parent="#accordion">
					<p style="border-bottom: 0; padding-bottom: 0;">As a Demat Account holder, you should:</p>
					<ul>
						<li>Check the status of your shareholding on a regular basis. If you encounter any discrepancy, you should inform your DP immediately,</li>
						<li>Pay the annual maintenance charge on timely basis,</li>
						<li>Pay the annual online charge on timely basis,</li>
						<li>Keep your DIS slips safe, and</li>
						<li>Keep your login username and password safe and protected from unauthorized person/s or access.</li>
					</ul>
				</div>

				<a class="collapsed" data-toggle="collapse" href="#dpfaq25">
					<h5>25. How much will I be charged for holding a Demat Account? <span><i class="fa fa-angle-down"></i></span></h5>
				</a>
				<div id="dpfaq25" class="collapse" data-parent="#accordion">
					<p style="border-bottom: 0; padding-bottom: 0;">The following charges will be applicable on holding a Demat Account:</p>
					<ul>
						<li>One-time account opening charge of Rs.50,</li>
						<li>Maintenance charge of Rs.100 once while opening your account and the same amount on the annual basis (every fiscal year),</li>
						<li>The annual charge for online Mero Share Charge of Rs.50</li>
						<li>DIS charge Rs. 25 per transaction</li>
						<li>Pledge charge Rs.50 per pledged company</li>
						<li><strong>For detailed FAQ, please visit <a href="https://cdsc.com.np/frequentlyaskquestion" target="_blank">http://www.cdscnp.com/CDSC_FAQ_English.pdf</a></strong></li>
					</ul>
